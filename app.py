import streamlit as st
import requests
import io
from PIL import Image
import random

# --- Page Setup ---
st.set_page_config(page_title="Alpha AI - Pro", page_icon="🛡️")
st.title("🛡️ Alpha AI: High-Speed Engine")
st.write("No Queues, No Limits, 100% Free.")

def generate_image_pro(prompt):
    # අපි පාවිච්චි කරන්නේ 'Stability AI' ගේ SDXL මොඩලය රන් වෙන වෙනත් සර්වර් එකක්
    # මේක ලේසියෙන් කාර්යබහුල වෙන්නේ නැහැ
    seed = random.randint(1, 1000000)
    
    # Prompt එක URL එකට ගැළපෙන සේ සැකසීම
    encoded_prompt = prompt.replace(" ", "%20")
    
    # මෙන්න අලුත්ම 'No-Queue' API එක
    url = f"https://api.airforce/v1/imagine?prompt={encoded_prompt}&model=flux&size=1:1&seed={seed}"
    
    try:
        response = requests.get(url, timeout=30)
        if response.status_code == 200:
            return response.content
        else:
            return None
    except:
        return None

# --- UI ---
user_input = st.text_area("What do you want to create?", placeholder="e.g. A realistic lion in ancient Sri Lanka...")

if st.button("Generate Now 🚀", use_container_width=True):
    if user_input:
        with st.spinner("Alpha Engine is rendering..."):
            img_data = generate_image_pro(user_input)
            
            if img_data:
                image = Image.open(io.BytesIO(img_data))
                st.image(image, caption="Generated by Alpha AI Pro", use_container_width=True)
                
                # Download Button
                st.download_button(
                    label="Download Image 📥",
                    data=img_data,
                    file_name="alpha_ai_pro.jpg",
                    mime="image/jpeg",
                    use_container_width=True
                )
            else:
                st.error("Engine is under maintenance. Please try again in 5 seconds.")
    else:
        st.warning("Please type a description!")

st.divider()
st.caption("Alpha AI Pro Tier | Dedicated High-Speed Node")
