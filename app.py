import streamlit as st
import requests
import io
from PIL import Image
import random

# --- Page Setup ---
st.set_page_config(page_title="Alpha Infinity Engine", page_icon="♾️", layout="centered")
st.title("♾️ Alpha AI: Infinity Engine")
st.markdown("#### The Most Stable 'No-Key' Image Generator")

def generate_infinity(prompt):
    seed = random.randint(1, 99999999)
    # Prompt එකේ තියෙන අනවශ්‍ය අකුරු අයින් කරලා පිරිසිදු කරනවා
    clean_prompt = "".join(i for i in prompt if i.isalnum() or i in " ")
    encoded_prompt = clean_prompt.replace(" ", "%20")

    # මේක තමයි මම හොයාගත්ත 'Stable' සර්වර් තුන
    # 1. Prodia Direct (Very Fast)
    # 2. Cloudflare R2 Buffer
    # 3. Flux High-Speed Node
    
    urls = [
        f"https://image.pollinations.ai/prompt/{encoded_prompt}?width=1024&height=1024&seed={seed}&model=flux&nologo=true",
        f"https://api.airforce/imagine2?prompt={encoded_prompt}&model=flux&seed={seed}",
        f"https://pollinations.ai/p/{encoded_prompt}?width=1024&height=1024&seed={seed}&model=flux-realism"
    ]

    for url in urls:
        try:
            # තත්පර 15ක් ඇතුළත රිසල්ට් එක ආවේ නැත්නම් ඊළඟ එකට මාරු වෙනවා
            response = requests.get(url, timeout=15)
            if response.status_code == 200 and "image" in response.headers.get("Content-Type", "").lower():
                return response.content
        except:
            continue
    return None

# --- UI Layout ---
with st.container():
    user_input = st.text_input("Describe your imagination:", placeholder="A futuristic Sri Lanka in 2070, neon lights...")
    
    col1, col2 = st.columns([4, 1])
    with col1:
        gen_button = st.button("Generate Infinity 🚀", use_container_width=True)
    with col2:
        if st.button("🔄"): # Refresh Button
            st.rerun()

if gen_button:
    if user_input.strip():
        with st.spinner("Alpha Infinity Core is gathering data..."):
            img_data = generate_infinity(user_input)
            
            if img_data:
                image = Image.open(io.BytesIO(img_data))
                st.image(image, caption="Generated by Alpha Infinity Core", use_container_width=True)
                
                st.download_button(
                    label="Download 4K Image 📥",
                    data=img_data,
                    file_name="alpha_infinity_art.jpg",
                    mime="image/jpeg",
                    use_container_width=True
                )
            else:
                st.error("සියලුම සර්වර් මාර්ග අවහිර වී ඇත. කරුණාකර තත්පර 3ක් ඉන්න.")
    else:
        st.warning("Please enter a prompt first!")

st.divider()
st.info("💡 Tip: If one image fails, just click 'Generate' again. The system will auto-switch to a new server.")
