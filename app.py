import streamlit as st
import requests

# --- Settings ---
# ඔබේ Fal.ai API Key එක මෙතැනට දාන්න
FAL_API_KEY = "2bfcfcf6-a569-453f-8267-94727cf8dbe0:f955f6ec3286ae8f24ef68f27f309d84"

def generate_with_fal(prompt):
    # Fal.ai Flux Schnell API endpoint
    url = "https://fal.run/fal-ai/flux/schnell"
    
    headers = {
        "Authorization": f"Key {FAL_API_KEY}",
        "Content-Type": "application/json"
    }
    
    payload = {
        "prompt": prompt,
        "image_size": "landscape_16_9", # ඔබට අවශ්‍ය size එක මෙතැනින් වෙනස් කළ හැක
        "num_inference_steps": 4,
        "enable_safety_checker": True
    }
    
    try:
        response = requests.post(url, json=payload, headers=headers)
        if response.status_code == 200:
            return response.json()['images'][0]['url']
        else:
            return f"Error: {response.status_code} - {response.text}"
    except Exception as e:
        return f"Request Failed: {str(e)}"

# --- Streamlit UI ---
st.title("🛡️ Alpha AI - Powered by Fal.ai")

user_input = st.text_input("Enter your prompt:")

if st.button("Generate with Fal.ai ⚡"):
    if user_input:
        with st.spinner("Fal.ai is rendering..."):
            result = generate_with_fal(user_input)
            if result.startswith("http"):
                st.image(result, use_container_width=True)
                st.success("Generated by Fal.ai Flux Model")
            else:
                st.error(result)
